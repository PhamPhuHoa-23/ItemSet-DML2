"""
test_benchmark.jl — Benchmark correctness tests for LCMFreq

Verifies that the Julia optimized implementation produces exactly the same
frequent itemsets as the SPMF Java reference, and that the baseline
implementation agrees with the optimized one, across the FIMI benchmark
datasets recorded in data/results/correctness.csv.

Kosarak is excluded: it has no SPMF reference (SPMF runs out of memory on
it, recorded as spmf_count = -1 in correctness.csv), so there is nothing
to compare against.

Run with:
    julia --project=. test/test_benchmark.jl
"""

using Test

const PROJECT_ROOT = dirname(dirname(abspath(@__FILE__)))

include(joinpath(PROJECT_ROOT, "src", "algorithm", "lcmfreq_base.jl"))
include(joinpath(PROJECT_ROOT, "src", "utils",     "utils.jl"))

const _OPT_PATH = joinpath(PROJECT_ROOT, "src", "algorithm", "lcmfreq.jl")
module Optimized end
Base.include(Optimized, _OPT_PATH)

# ---------------------------------------------------------------------------
# Load correctness.csv (generated by scripts/run_experiments.jl)
# Columns: dataset, minsup_frac, n_tx, minsup_abs, julia_count, spmf_count,
#          match_pct
# ---------------------------------------------------------------------------
function load_correctness_csv(path::String)
    rows = []
    open(path, "r") do fh
        header = split(readline(fh), ",")
        for line in eachline(fh)
            isempty(strip(line)) && continue
            vals = split(strip(line), ",")
            push!(rows, Dict(strip(h) => strip(v) for (h, v) in zip(header, vals)))
        end
    end
    return rows
end

const CORRECTNESS_CSV = joinpath(PROJECT_ROOT, "data", "results", "correctness.csv")

if !isfile(CORRECTNESS_CSV)
    error("""
    correctness.csv not found at: $CORRECTNESS_CSV
    Please run the experiment first:
        julia --project=. scripts/run_experiments.jl
    """)
end

# Datasets with no usable SPMF reference (see module docstring above).
const EXCLUDED_DATASETS = Set(["Kosarak"])

# Baseline stores tidsets in a Vector{Int} indexed by item ID, so it runs out
# of memory on datasets with a large item space; only run it where feasible.
const BASE_FEASIBLE = Set(["Chess", "Connect", "Mushroom", "Pumsb"])

rows = filter(r -> r["dataset"] ∉ EXCLUDED_DATASETS, load_correctness_csv(CORRECTNESS_CSV))

by_dataset = Dict{String, Vector{Dict{String,String}}}()
for row in rows
    push!(get!(by_dataset, row["dataset"], []), row)
end

println("Loaded correctness.csv: $(length(rows)) rows across $(length(by_dataset)) datasets")

# ---------------------------------------------------------------------------
# Helper: normalize a result for comparison (order-independent)
# ---------------------------------------------------------------------------
normalize(result) = sort([(sort(fi.items), fi.support) for fi in result.itemsets])

# ---------------------------------------------------------------------------
# Run tests
# ---------------------------------------------------------------------------
@testset "Benchmark Correctness" begin
    for (ds_name, ds_rows) in sort(collect(by_dataset), by = first)
        dat_path = joinpath(PROJECT_ROOT, "data", "benchmark",
                             lowercase(replace(ds_name, r"\s+" => "")) * ".dat")

        if !isfile(dat_path)
            @warn "[$ds_name] Skipping: .dat file not found at $dat_path"
            continue
        end

        do_base = ds_name in BASE_FEASIBLE
        db      = load_transactions(dat_path)
        println("\n[$ds_name]  (baseline=$(do_base ? "yes" : "N/A, large item space"))")

        @testset "$ds_name" begin
            for row in ds_rows
                minsup_frac    = parse(Float64, row["minsup_frac"])
                minsup_abs_val = max(1, ceil(Int, length(db) * minsup_frac))
                spmf_count     = parse(Int, row["spmf_count"])
                opt_expected   = parse(Int, row["julia_count"])

                r_opt     = Optimized.lcmfreq(db, minsup_abs_val)
                opt_count = length(r_opt.itemsets)

                println("  minsup=$(round(minsup_frac*100, digits=1))%  " *
                        "opt=$(opt_count) spmf=$(spmf_count)")

                @test opt_count == spmf_count    # matches SPMF reference
                @test opt_count == opt_expected  # matches recorded baseline run

                if do_base
                    r_base     = lcmfreq_base(db, minsup_abs_val)
                    base_count = length(r_base.itemsets)
                    @test base_count == opt_count                  # same count
                    @test normalize(r_base) == normalize(r_opt)    # same itemsets
                end
            end
        end
    end
end
