"""
test_benchmark.jl — Benchmark correctness tests for LCMFreq

Verifies that the Julia Optimised implementation produces exactly the same
number of frequent itemsets as the SPMF Java reference implementation across
all 9 benchmark datasets and a range of minsup thresholds.

Reference counts are loaded from data/results/correctness.csv, which is
generated by notebooks/run_experiments.jl and cross-checked against SPMF.

Run with:
    julia --project=. tests/test_benchmark.jl
"""

using Test

const PROJECT_ROOT = dirname(dirname(abspath(@__FILE__)))

# Load implementations
include(joinpath(PROJECT_ROOT, "src", "algorithm", "lcmfreq_base.jl"))
include(joinpath(PROJECT_ROOT, "src", "utils",     "utils.jl"))

const _OPT_PATH = joinpath(PROJECT_ROOT, "src", "algorithm", "lcmfreq.jl")
module Optimized end
Base.include(Optimized, _OPT_PATH)

# ---------------------------------------------------------------------------
# Load correctness.csv (generated by run_experiments.jl)
# Columns: dataset, minsup_frac, minsup_abs, julia_opt_count, spmf_count,
#          baseline_count, match_opt_spmf, match_base_spmf
# ---------------------------------------------------------------------------
function load_correctness_csv(path::String)
    rows = []
    open(path, "r") do fh
        header = split(readline(fh), ",")
        for line in eachline(fh)
            isempty(strip(line)) && continue
            vals = split(strip(line), ",")
            row = Dict(strip(h) => strip(v) for (h, v) in zip(header, vals))
            push!(rows, row)
        end
    end
    return rows
end

const CORRECTNESS_CSV = joinpath(PROJECT_ROOT, "data", "results", "correctness.csv")

if !isfile(CORRECTNESS_CSV)
    error("""
    correctness.csv not found at: $CORRECTNESS_CSV
    Please run the experiment first:
        julia --project=. notebooks/run_experiments.jl
    """)
end

rows = load_correctness_csv(CORRECTNESS_CSV)

# Group rows by dataset
by_dataset = Dict{String, Vector{Dict{String,String}}}()
for row in rows
    ds = row["dataset"]
    push!(get!(by_dataset, ds, []), row)
end

println("Loaded correctness.csv: $(length(rows)) rows across $(length(by_dataset)) datasets")

# ---------------------------------------------------------------------------
# Helper: normalize result for comparison
# ---------------------------------------------------------------------------
function normalize(result::LCMFreqResult)
    pairs = [(sort(fi.items), fi.support) for fi in result.itemsets]
    sort!(pairs, by = x -> x[1])
    return pairs
end
function normalize(result::Optimized.LCMFreqResult)
    pairs = [(sort(fi.items), fi.support) for fi in result.itemsets]
    sort!(pairs, by = x -> x[1])
    return pairs
end

# ---------------------------------------------------------------------------
# Helper: run benchmark test for one (dataset, minsup) pair
# ---------------------------------------------------------------------------
function run_benchmark_test(ds_name, dat_path, minsup_abs_val, spmf_count, do_base)
    db = load_transactions(dat_path)

    r_opt = Optimized.lcmfreq(db, minsup_abs_val)
    opt_count = length(r_opt.itemsets)

    match_spmf = (opt_count == spmf_count)

    if do_base
        r_base     = lcmfreq_base(db, minsup_abs_val)
        base_count = length(r_base.itemsets)
        match_base = (normalize(r_base) == normalize(r_opt))
    else
        base_count = nothing
        match_base = true   # N/A — baseline OOM for this dataset
    end

    return (; ds_name, minsup_abs_val, spmf_count, opt_count,
              base_count, match_spmf, match_base)
end

# ---------------------------------------------------------------------------
# Dataset config: which datasets to run baseline comparison on
# ---------------------------------------------------------------------------
# Baseline uses Vector{Int} indexed by max item ID → OOM for large item spaces
BASE_FEASIBLE = Set(["Chess", "Connect", "Mushroom", "Pumsb"])

# ---------------------------------------------------------------------------
# Run tests
# ---------------------------------------------------------------------------
total   = 0
passed  = 0
skipped = 0

println("\n", "="^72)
println("Benchmark Correctness Tests — Julia Optimised vs SPMF Reference")
println("="^72)

for (ds_name, ds_rows) in sort(collect(by_dataset), by = x -> x[1])
    dat_path = joinpath(PROJECT_ROOT, "data", "benchmark",
                        lowercase(replace(ds_name, r"\s+" => "")) * ".dat")

    if !isfile(dat_path)
        @warn "  [$ds_name] Skipping — .dat file not found at $dat_path"
        skipped += length(ds_rows)
        continue
    end

    do_base = ds_name in BASE_FEASIBLE
    println("\n[$ds_name]  (baseline=$(do_base ? "yes" : "N/A — large item space"))")

    db = load_transactions(dat_path)

    @testset "$ds_name" begin
        for row in ds_rows
            minsup_frac = parse(Float64, row["minsup_frac"])
            minsup_abs_val = max(1, floor(Int, length(db) * minsup_frac))
            spmf_count  = parse(Int, row["spmf_count"])
            opt_expected = parse(Int, row["julia_count"])

            total += 1
            try
                r_opt = Optimized.lcmfreq(db, minsup_abs_val)
                opt_count = length(r_opt.itemsets)

                # Test 1: Optimised count matches SPMF (skip if SPMF returned -1)
                if spmf_count > 0
                    @test opt_count == spmf_count
                end

                # Test 2: Optimised count matches what was recorded in CSV
                @test opt_count == opt_expected

                if do_base
                    r_base = lcmfreq_base(db, minsup_abs_val)
                    base_count = length(r_base.itemsets)
                    # Test 3: Baseline count matches optimised
                    @test base_count == opt_count
                    # Test 4: Itemsets are identical (not just counts)
                    @test normalize(r_base) == normalize(r_opt)
                end

                match_str = opt_count == spmf_count ? "PASS" : "FAIL"
                println("  minsup=$(round(minsup_frac*100, digits=1))%  " *
                        "opt=$(opt_count) spmf=$(spmf_count)  [$match_str]")
                opt_count == spmf_count && (passed += 1)

            catch e
                @error "  minsup=$(minsup_frac): $(e)"
                total += 0  # already counted
            end
        end
    end
end

println("\n", "="^72)
println("Results: $passed / $total tests passed  ($skipped skipped)")
println("="^72)
